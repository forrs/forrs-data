CREATE TABLE Category (
  id BIGINT PRIMARY KEY DEFAULT NEXTVAL('category_id_seq'),
  name VARCHAR (64) UNIQUE NOT NULL
);
CREATE SEQUENCE topic_id_seq MINVALUE -9223372036854775808 MAXVALUE 9223372036854775807;
CREATE TABLE Topic (
  id BIGINT PRIMARY KEY DEFAULT NEXTVAL('topic_id_seq'),
  category_id BIGINT REFERENCES Category(id) NULL,
  creator_id BIGINT REFERENCES "User"(id) NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  edited_at TIMESTAMPTZ DEFAULT NULL,
  title VARCHAR(128) NOT NULL
);
CREATE SEQUENCE post_id_seq MINVALUE -9223372036854775808 MAXVALUE 9223372036854775807;
CREATE TABLE Post (
  id BIGINT DEFAULT NEXTVAL('post_id_seq'),
  topic_id BIGINT REFERENCES Topic(id),
  author_id BIGINT REFERENCES "User"(id) NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  edited_at TIMESTAMPTZ DEFAULT NULL,
  content TEXT NOT NULL,
  PRIMARY KEY (topic_id, id)
);